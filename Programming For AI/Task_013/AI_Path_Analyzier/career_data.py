def predict_career(user_input):
    user_input = user_input.lower()
    scores = {}

    for career, data in career_data.items():
        score = 0
        for interest in data["interests"]:
            if interest in user_input:
                score += 1
        scores[career] = score

    best = max(scores, key=scores.get)

    if scores[best] == 0:
        return "General Career"

    return best